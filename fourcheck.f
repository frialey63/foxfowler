      SUBROUTINE FOURCHECK(WORD,IFLAG)

C     "REFERENCE CHARACTERS"
      DIMENSION CH(28)
      DATA CH(1),CH(2),CH(3)/1HA,1HB,1HC/
      DATA CH(4),CH(5),CH(6)/1HD,1HE,1HF/
      DATA CH(7),CH(8),CH(9)/1HG,1HH,1HI/
      DATA CH(10),CH(11),CH(12)/1HJ,1HK,1HL/
      DATA CH(13),CH(14),CH(15)/1HM,1HN,1HO/
      DATA CH(16),CH(17),CH(18)/1HP,1HQ,1HR/
      DATA CH(19),CH(20),CH(21)/1HS,1HT,1HU/
      DATA CH(22),CH(23),CH(24)/1HV,1HW,1HX/
      DATA CH(25),CH(26),CH(27)/1HY,1HZ,1H /
      DATA CH(28)/1H./

      IFLAG = 0

C     "CHECK EACH LETTER OF WORD FOR VALIDITY"
      DO 1 K=1,4
      DO 2 L=1,27
      I = 1
      CALL COMP(I,WORD,K,CH(L), 1)
      IF(I.EQ.1) GO TO 1
    2 CONTINUE

C     "INVALID CHACTER FOUND"
      GO TO 3
    1 CONTINUE

C     "COMPLETE WORD IS VALID"
      IFLAG = 1
      GO TO 7

C     "IS THE INVALID CHARACTER A FULL STOP?"
    3 I = 1
      CALL COMP(I,WORD,K,CH(28),1)
      IF(I.EQ.1) GO TO 5
      WRITE (6,4)
      GO TO 7
    5 WRITE (6,6)

    7 RETURN

    4 FORMAT (1H ,8HERROR - 23HTHIS MUST BE ALPHABETIC/
     &1H ,13HPLEASE RETYPE)
    6 FORMAT (1H ,8HERROR - 22HDO NOT USE FULL STOPS,,18H E.G. FOR INITIALS/
     &1H ,13HPLEASE RETYPE)

      END
